			<h1 class="centrado">Acerca de</h1>
			<p>La Ruta del Café tiene como eje temático principal el “café”, siendo característica 
				su producción y consumo en la región norte-centro del país. Comprende el despliegue de 
				atractivos y recursos turísticos a lo largo de cinco departamentos: Jinotega, Matagalpa, 
				Estelí, Nueva Segovia y Madriz, los cuales comprenden a su vez 48 municipios con una extensión
				 territorial de 23,455.46 kms2, equivalente al 19.49% del territorio nacional.</p>
			<p>La Ruta del Café está diseñada desde un modelo a escalas, integrando la temática del café, 
				un circuito integrado por los diferentes departamentos, destinos en las principales cabeceras 
				departamentales, y finalmente, atractivos naturales y culturales, entre otros. Es un modelo de 
				planificación turística territorial orientada a facilitar el desarrollo, promoción y comercialización 
				de los productos turísticos, desarrollando una dinámica turística diversa y completa: turismo de naturaleza, 
				aventura, 
				rural y comunitario, histórico urbano, y cultural, entre otros.</p>
